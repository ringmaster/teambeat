        <h2>Concepts</h2>
        
        <dl>
            <dt>Board</dt>
            <dd>The full unit of a retrospective, consists of multiple Columns containing Cards with discussion topics.</dd>
            
            
            <dt>Column</dt>
            <dd>A themed grouping of cards.</dd>
            
            <dt>Card</dt>
            <dd>An atomic unit of discussion, replresented by a single box in a Column on a Board.</dd>
            
            <dt>Scene</dt>
            <dd>As a retrospective procedes, it will go through several scenes, like in a play.  Each scene serves a
                specific purpose within the retrospective.  In Teambeat, there are three types of scenes, 
                "Columns", "Present", and "Review".</dd>
                
            <dt>Facilitator</dt>
            <dd>In Teambeat, the Facilitator is the person who has extra controls that allow them to progress the retrospective
                through its scenes.  They are able to change the structure of the board by adding/removing Columns and Scenes,
                and can release votes to participants for use.  They also have exclusive control over the timer.</dd>
                    
            <dt>Votes</dt>
            <dd>Often during a retrospective participants will be permitted to vote on cards.  This is ofte to determine the
                order of priority for their review, but can also be for other reasons.  Participants can have multiple votes 
                of different types to use, which the facilitator can release during the appropriate scene.</dd>
        </dl>
        
